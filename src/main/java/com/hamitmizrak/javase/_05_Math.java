package com.hamitmizrak.javase;

public class _05_Math {
    public static void main(String[] args) {
        System.out.println(Math.min(3,100));
        System.out.println(Math.max(3,100));
        System.out.println(Math.sqrt(100));
        System.out.println(Math.abs(-100));
        System.out.println(Math.pow(2,5));
        System.out.println(Math.floor(5.9));
        System.out.println(Math.ceil(6.1));
        System.out.println(Math.round(7.4));
        System.out.println(Math.round(7.5));
        System.out.println(Math.sin(-1));
        System.out.println( Math.sqrt(Math.max(3,100)));
    }
}
